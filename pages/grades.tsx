import Head from "next/head";
import LoginScreen from "../components/screens/SignInScreen";
import HomeScreen from "../components/screens/HomeScreen";
import { useSession, getSession } from "next-auth/react";
import HeaderLayout from "../layouts/HeaderLayout";
import GradesScreen from "../components/screens/GradesScreen";
import { requireAuth } from "../utils/requireAuth";
import prisma from "../lib/prismadb";

type Props = {
  grades: Array<{
    name: string;
    grade: string;
    term: number;
  }>;
};

export default function Home({ grades }: Props) {
  return (
    <div>
      <Head>
        <title>E-class</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <HeaderLayout>
        <GradesScreen grades={grades} />
      </HeaderLayout>
    </div>
  );
}

export async function getServerSideProps(context: any) {
  return requireAuth(context, async () => {
    const data = await prisma.grades.findMany();
    const grades = data;
    return { props: { grades } };
  });
}
